class Shop:
    def __init__(self, extra_size, extra_speed):
        self.extra_size = 0
        self.extra_speed = 0
        
    def upgrade_paddle_size():
        self.extra_size += 5
    
    def upgrade_paddle_speed():
        self.extra_speed += 20
    
    def reset():
        extra_size = 0
        extra_speed = 0